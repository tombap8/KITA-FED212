///////////// 로딩구역 /////////////////
$(()=>{ /////////// jQB //////////////////////

    console.log("회원가입 로딩완료!");

    ///////////////////////////////////////////////////////
    ///// 입력요소에서 포커스가 빠질때 발생하는 이벤트는? ////
    ///////////////////////////////////////////////////////
    // focus의 반대!!! ->>>>>>>>> blur(블러~!!!!!)
    // 대상: input[type=text],input[type=password]
    $("input[type=text],input[type=password]")
    .blur(function(){

        // 방금 블러발생한 요소의 id는?
        let cid = $(this).attr("id");

        console.log("블러발생~!!!!",cid);

    }); ////////////// blur ///////////////////////
    ////////////////////////////////////////////////


}); ////////////// jQB ////////////////////////
